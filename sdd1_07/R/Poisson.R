## Sept premiers termes de la distribution de Poisson avec lambda = 2
lambda <- 2
(P0 <- exp(-lambda))
(P1 <- P0 * lambda / 1)
(P2 <- P1 * lambda / 2)
(P3 <- P2 * lambda / 3)
(P4 <- P3 * lambda / 4)
(P5 <- P4 * lambda / 5)
(P6 <- P5 * lambda / 6)
(P7 <- P6 * lambda / 7)
(P8 <- P7 * lambda / 8)
(P9 <- P8 * lambda / 9)
(P10 <- P9 * lambda / 10)
